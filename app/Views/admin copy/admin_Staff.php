        function importCSV() {
            const input = document.getElementById('csvFileInput');
            const file = input.files[0];

            if (file) {
                const reader = new FileReader();
                reader.onload = function(e) {
                    const text = e.target.result;
                    parseCSV(text);
                }
                reader.readAsText(file);
